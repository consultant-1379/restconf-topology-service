package com.ericsson.oss.services.restconf.topologyservice.api.yang.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * EModel mapping annotation.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface EModelMapping {
    String eModel() default "";

    AttributeMap[] mapping() default {};

    String[] cacheAttributes() default {};
}
